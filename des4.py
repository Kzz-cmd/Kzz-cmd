sla = input('Digite algo: \n')
print('Oque você digitou é do tipo',type(sla))
print('Só tem espaço?',sla.isspace())
print('É um número?',sla.isnumeric())
print('É alfabético?',sla.isalpha())
print('Está em Maiúsculo?',sla.isupper())
print('Está em Minúsculo?',sla.islower())
print('Está capitalizado?',sla.istitle())

